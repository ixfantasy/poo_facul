public class Carro {

    private int num_assentos;
    private String cor;

    // construtor
    public Carro(){
    }
    
    // getters + setters
    public int getNum_assentos() {
        return num_assentos;
    }

    public void setNum_assentos(int num_acentos) {
        this.num_assentos = num_acentos;
    }

    public String getCor() {
        return cor;
    }
    
    public void setCor(String cor) {
        this.cor = cor;
    } 
}